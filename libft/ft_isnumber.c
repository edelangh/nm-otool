#include "libft.h"

int		ft_isnumber(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	if (str[0] == '-')
		++i;
	while ((str[i] >= '0' && str[i] <= '9'))
		++i;
	return (str[i] == '\0');
}
