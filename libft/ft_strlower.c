#include "libft.h"

char	*ft_strlower(char *c)
{
	int	i;

	i = -1;
	if (c)
		while (c[++i])
			c[i] = (c[i] >= 'A' && c[i] <= 'Z' ? c[i] + 32 : c[i]);
	return (c);
}
